#include "ft_math.h"

int	ft_getdigits(int val)
{
	int	n;

	if (val < 0)
		val *= -1;
	if (!val)
		return (1);
	n = 0;
	while (val)
	{
		val /= 10;
		++n;
	}
	return (n);
}
